public class Calculator {
    public static float summarize(float firstTerm, float secondTerm){
        return firstTerm + secondTerm;
    }

    public static float substraction(float minuend, float subtrahend){
        return minuend - subtrahend;
    }

    public static float multiply(float firstMultiplier, float secondMultiplier){
        return firstMultiplier * secondMultiplier;
    }

    public static int division(int dividend, int divisor){
        return dividend/divisor;
    }
}
